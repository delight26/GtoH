package com.project.call.domain;

public class PointProduct {
	private int pProductCode;
	private String pName;
	private int pPrice;
	private String pImage;
	private int pBuy;
	private int pAmount;
	private int pQuantity;

	public int getpQuantity() {
		return pQuantity;
	}

	public void setpQuantity(int pQuantity) {
		this.pQuantity = pQuantity;
	}

	public int getpAmount() {
		return pAmount;
	}

	public void setpAmount(int pAmount) {
		this.pAmount = pAmount;
	}

	public int getpProductCode() {
		return pProductCode;
	}

	public void setpProductCode(int pProductCode) {
		this.pProductCode = pProductCode;
	}

	public String getpName() {
		return pName;
	}

	public void setpName(String pName) {
		this.pName = pName;
	}

	public int getpPrice() {
		return pPrice;
	}

	public void setpPrice(int pPrice) {
		this.pPrice = pPrice;
	}

	public String getpImage() {
		return pImage;
	}

	public void setpImage(String pImage) {
		this.pImage = pImage;
	}

	public int getpBuy() {
		return pBuy;
	}

	public void setpBuy(int pBuy) {
		this.pBuy = pBuy;
	}

}
