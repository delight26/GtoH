package com.project.call.domain;

public class OrderProduct {
	private int opNo;
	private int opPno;
	private String opName;
	private String opAddr;
	private String opPhone1;
	private String opPhone2;
	private String opTell;
	private String opEmail;

	public int getOpNo() {
		return opNo;
	}

	public void setOpNo(int opNo) {
		this.opNo = opNo;
	}

	public int getOpPno() {
		return opPno;
	}

	public void setOpPno(int opPno) {
		this.opPno = opPno;
	}

	public String getOpName() {
		return opName;
	}

	public void setOpName(String opName) {
		this.opName = opName;
	}

	public String getOpAddr() {
		return opAddr;
	}

	public void setOpAddr(String opAddr) {
		this.opAddr = opAddr;
	}

	public String getOpPhone1() {
		return opPhone1;
	}

	public void setOpPhone1(String opPhone1) {
		this.opPhone1 = opPhone1;
	}

	public String getOpPhone2() {
		return opPhone2;
	}

	public void setOpPhone2(String opPhone2) {
		this.opPhone2 = opPhone2;
	}

	public String getOpTell() {
		return opTell;
	}

	public void setOpTell(String opTell) {
		this.opTell = opTell;
	}

	public String getOpEmail() {
		return opEmail;
	}

	public void setOpEmail(String opEmail) {
		this.opEmail = opEmail;
	}

}
